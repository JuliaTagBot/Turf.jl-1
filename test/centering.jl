using GeoInterface: Point, LineString, Feature
include("../src/lib/Centering.jl")

@testset "centering" begin

    p1 = Point([4.831961989402771, 45.75764678012361])
    l1 = LineString([[
        4.86020565032959,
        45.76884015325622
      ],
      [
        4.85994815826416,
        45.749558161214516
      ]])

    poly = Polygon([[[
          4.8250579833984375,
          45.79398056386735
        ],
        [
          4.882392883300781,
          45.79254427435898
        ],
        [
          4.910373687744141,
          45.76081677972451
        ],
        [
          4.894924163818359,
          45.7271539426975
        ],
        [
          4.824199676513671,
          45.71337148333104
        ],
        [
          4.773387908935547,
          45.74021417890731
        ],
        [
          4.778022766113281,
          45.778418789239055
        ],
        [
          4.8250579833984375,
          45.79398056386735
        ]]])

    @test centroid(p1).coordinates == [4.831961989402771, 45.75764678012361]
    @test centroid(l1).coordinates == [4.860076904296875,45.75919915723537]
    @test centroid(poly).coordinates == [4.839177131652832,45.76256007199914]

end
